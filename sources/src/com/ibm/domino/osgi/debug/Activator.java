package com.ibm.domino.osgi.debug;

import java.net.URL;

import org.eclipse.core.runtime.FileLocator;
import org.eclipse.core.runtime.Path;
import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.swt.graphics.Image;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends AbstractUIPlugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "com.ibm.domino.osgi.debug";

	// The shared instance
	private static Activator plugin;
	
	/**
	 * The constructor
	 */
	public Activator() {
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}
	
	/**
     * @param key
     * @return
     */
    public Image getImage(String key) {
        Image image = getImageRegistry().get( key );
        if ( image == null ){
            //Try to load it
            getImageRegistry().put( key, createImageDescriptor( key ) );
            image = getImageRegistry().get( key );
        }
        return image;
    }
    
    /**
     * @param path
     * @return
     */
    private ImageDescriptor createImageDescriptor( String path ) {
        URL url = FileLocator.find( getBundle(), new Path( path ), null);
        return url != null ? ImageDescriptor.createFromURL(url) : null;
    }

}
